namespace TransfermarktScraper.ServiceDefaults.Logging.Enums
{
    /// <summary>
    /// Represents the severity level of a log.
    /// </summary>
    public enum Level
    {
        /// <summary>
        /// Represents information level logs.
        /// </summary>
        Information = 0,

        /// <summary>
        /// Represents warning level logs.
        /// </summary>
        Warning = 1,

        /// <summary>
        /// Represents error level logs.
        /// </summary>
        Error = 2,

        /// <summary>
        /// Represents any other error level logs.
        /// </summary>
        Other = 3,
    }
}
